    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function show(Request $request)
    {
        $post = $this->postRepository->show($request->id);

        return response()->json($post, 200);
    }

